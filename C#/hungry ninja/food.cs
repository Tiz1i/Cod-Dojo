class Food
{
    public string Name;
    public int Calories;
    public bool IsSpicy; 
    public bool IsSweet; 

    public Food(string name, int kalori, bool djeges, bool embelsira)
    {
        Name = name;
        Calories = kalori;
        IsSpicy = djeges;
        IsSweet = embelsira;
    }
}
